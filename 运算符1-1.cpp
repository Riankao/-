/* （1）概念：初等运算符优先等级1，单目运算符优先级2.
   （2）只有单目运算符的赋值运算和结合方向是从右到左的
*/
#include <stdio.h>
int main(void)
{
	//概念理解
	printf("%d, %d\n", 1+2*3, (1+2)*3);		//输出7,9
	int a = 1, b = 2, c = 3;
	a = b = c;
	printf("%d\n",a);						//输出“3”  学习（2）
	//! 逻辑非运算符，求该操作的逻辑反值，如果操作数值为真，则整个表达式为假，否则整个表达式为真
	printf("%d,%d,%d\n",!1,!9,!0);	//

	//-负号运算符
	printf("%d\n",4*-3); //4*-3与4*(-3)等价，因为乘号比符号的优先级低，所以圆括号可以省略
	return 0;
}
